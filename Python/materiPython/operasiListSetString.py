len() #intinya untuk menghitung jumlah item dalam suatu list, set, atau string
#operasi pada list
my_list = [1, 2, 3, 4, 5]
print(len(my_list)) # Output: 5
#operasi pada set
my_list = set([1, 2, 3, 4, 5])
print(len(my_list)) # Output: 5
#operasi pada string
my_string = "Hello, World!"
print(len(my_string)) # Output: 13